/** MULTIPLY *
 * @param $x
 * @param $y
 * @return mixed
 */
function multiply($x, $y) {
  return $x * $y;
}

/** ADD  *
 * @param $x
 * @param $y
 * @return mixed
 */
function add($x, $y) {
  return $x + $y;
}